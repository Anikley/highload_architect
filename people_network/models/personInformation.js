const personInformation = function (personInformation) {
    this.PersonId = personInformation.PersonId;
    this.Name = personInformation.Name;
    this.LastName = personInformation.LastName;
    this.Patronymic = personInformation.Patronymic;
    this.Birth = personInformation.Birth;
    this.Gender = personInformation.Gender;
    this.Town = personInformation.Town;
    this.Hobbies = personInformation.Hobbies;
};
// eslint-disable-next-line no-undef
module.exports = personInformation;
